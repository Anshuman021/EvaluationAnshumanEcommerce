// category 1
// For product 1
function cat_1_prod_1(){
    const Shoe1Name = document.getElementById('shoe1-name').innerText;
    const Shoe1Price = document.getElementById('shoe1-price').innerText;
    const Shoe1ImgUrl = document.getElementById('shoe1-ImgUrl').src;
    const Shoe1Id = document.getElementsByClassName('prod-shoe1')[0].innerText;
    
    localStorage.setItem("prod-name",Shoe1Name);
    localStorage.setItem("prod-price",Shoe1Price);
    localStorage.setItem("prod-imgurl",Shoe1ImgUrl);
    localStorage.setItem("prod-id",Shoe1Id);

    return;
}

